-- Побудувати функцію ЗАЛИШОК(Х, 3), 
-- яка визначає залишки від ділення кожного елемента числового списку Х на 3. 
-- Використати попередньо побудовану  функцію ВІДОБРАЗИТИ(Х, У), яка застосовує функцію 
-- У до кожного елементу списку Х. 

lsrest xs z = map (\x -> rest x z) xs

rest x z | x >= z = rest (x - z) z
         | otherwise = x